#include<stdio.h>

int main_ex03_operator() {
	printf("10 == 10의 값: %d \n", 10 == 10); // 1
	printf("10 != 10의 값: %d \n", 10 != 10); // 0
	printf("10 > 5의 값: %d \n", 10 > 5);	// 1
	printf("10 < 5의 값: %d \n", 10 < 5);	// 0
	printf("10 >= 10의 값: %d \n", 10 >= 10);	// 1
	printf("10 <= 5의 값: %d \n", 10 <= 5);		// 0
}